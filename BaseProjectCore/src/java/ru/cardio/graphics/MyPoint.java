package ru.cardio.graphics;

/**
 *
 * @author rogvold
 */
public class MyPoint {
    private long x;
    private Double y;

    public MyPoint(long x, Double y) {
        this.x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    
    
    
}
